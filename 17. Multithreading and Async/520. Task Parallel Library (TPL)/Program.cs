// Released in .NET 4 in 2010.
// The core component of TPL is the Task class.
// A task represents a unit of work that can be executed asynchronously on a seperate thread.
// In some ways, a task resembles a thread work item, but at a higher level of 
// abstraction.(what does this higher level of abstraction means?)
// Using Task provides a 2 primary benefits.
// Just as threads they allow more efficient and more scalable use of System resources.
// Behind the scenes, the TPL queues tasks to the thread pool, which, as we learned 
// before, provides the optimal way of scheduling work on threads.

